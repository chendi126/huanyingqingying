// 根据ID获取剧目
export function getPlayById(id) {
  return plays.find(play => play.id === id);
}

// 获取剧目的连接线
export function getPlayConnections() {
  const connections = [];
  plays.forEach(play => {
    play.connections.forEach(connId => {
      const targetPlay = plays.find(p => p.id === connId);
      if (targetPlay) {
        connections.push({
          from: play.position,
          to: targetPlay.position
        });
      }
    });
  });
  return connections;
}

